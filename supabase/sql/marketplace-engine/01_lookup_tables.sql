);


-- ============================================================
-- 6. APPLICATION PROPOSAL LIBRARY
-- ============================================================

create table if not exists public.demo_proposal_templates (
  id bigint generated by default as identity primary key,

  category text not null,

  proposal_template text not null,

  minimum_completion_days integer not null default 2,
  maximum_completion_days integer not null default 14,

  bid_percentage_min numeric(5, 2) not null default 75,
  bid_percentage_max numeric(5, 2) not null default 110,

  is_active boolean not null default true,

  created_at timestamptz not null default now(),

  constraint demo_proposal_templates_days_check
    check (
      minimum_completion_days > 0
      and maximum_completion_days >= minimum_completion_days
    ),

  constraint demo_proposal_templates_bid_check
    check (
      bid_percentage_min > 0
      and bid_percentage_max >= bid_percentage_min
    )
);


-- ============================================================
-- 7. MARKETPLACE ENGINE SETTINGS
-- ============================================================

create table if not exists public.demo_marketplace_settings (
  setting_key text primary key,
  setting_value jsonb not null,
  description text,

  updated_at timestamptz not null default now()
);


-- Default settings used by future generator functions.

insert into public.demo_marketplace_settings (
  setting_key,
  setting_value,
  description
)
values
  (
    'job_expiry_days',
    '14'::jsonb,
    'Default number of days before a demo job expires.'
  ),
  (
    'featured_job_percentage',
    '15'::jsonb,
    'Approximate percentage of generated demo jobs marked as featured.'
  ),
  (
    'verified_freelancer_percentage',
    '85'::jsonb,
    'Approximate percentage of generated demo freelancers marked as verified.'
  ),
  (
    'top_rated_freelancer_percentage',
    '20'::jsonb,
    'Approximate percentage of demo freelancers marked as top rated.'
  ),
  (
    'maximum_applications_per_job',
    '8'::jsonb,
    'Maximum number of generated demo applications per job.'
  ),
  (
    'minimum_applications_per_job',
    '1'::jsonb,
    'Minimum preferred number of generated demo applications per job.'
  )
on conflict (setting_key)
do update set
  description = excluded.description,
  updated_at = now();


-- ============================================================
-- 8. UPDATED_AT FUNCTION
-- ============================================================

create or replace function public.set_demo_marketplace_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;


-- ============================================================
-- 9. UPDATED_AT TRIGGERS
-- ============================================================

drop trigger if exists
set_demo_job_templates_updated_at
on public.demo_job_templates;

create trigger set_demo_job_templates_updated_at
before update on public.demo_job_templates
