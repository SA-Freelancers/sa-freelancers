-- ============================================================
-- 1. JOB TEMPLATE LIBRARY
-- ============================================================

create table if not exists public.demo_job_templates (
  id bigint generated by default as identity primary key,

  category text not null,
  title text not null,
  description_template text not null,

  min_budget numeric(12, 2) not null default 500,
  max_budget numeric(12, 2) not null default 5000,

  min_duration_days integer not null default 2,
  max_duration_days integer not null default 14,

  experience_level text not null default 'Intermediate',

  skills text[] not null default '{}',

  is_active boolean not null default true,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint demo_job_templates_budget_check
    check (
      min_budget >= 0
      and max_budget >= min_budget
    ),

  constraint demo_job_templates_duration_check
    check (
      min_duration_days > 0
      and max_duration_days >= min_duration_days
    ),

  constraint demo_job_templates_experience_check
    check (
      experience_level in (
        'Entry Level',
        'Intermediate',
        'Expert'
      )
    )
);


-- Prevent duplicate job templates.

create unique index if not exists
demo_job_templates_unique_title_category_idx
on public.demo_job_templates (
  lower(category),
  lower(title)
);


-- ============================================================
-- 2. LOCATION LIBRARY
-- ============================================================

create table if not exists public.demo_marketplace_locations (
  id bigint generated by default as identity primary key,

  city text not null,
  province text,
  country text not null default 'South Africa',

  work_type text not null default 'Remote',

  is_active boolean not null default true,

  created_at timestamptz not null default now(),

  constraint demo_marketplace_locations_work_type_check
    check (
      work_type in (
        'Remote',
        'On-site',
        'Hybrid'
      )
    )
);


create unique index if not exists
demo_marketplace_locations_unique_idx
on public.demo_marketplace_locations (
  lower(city),
  lower(coalesce(province, '')),
  lower(work_type)
);


-- ============================================================
-- 3. FREELANCER PROFESSION LIBRARY
-- ============================================================

create table if not exists public.demo_freelancer_templates (
  id bigint generated by default as identity primary key,

  category text not null,
  headline text not null,

  bio_template text not null,

  skills text[] not null default '{}',

  minimum_hourly_rate numeric(10, 2) not null default 150,
  maximum_hourly_rate numeric(10, 2) not null default 500,

  minimum_experience_years integer not null default 1,
  maximum_experience_years integer not null default 8,

  is_active boolean not null default true,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint demo_freelancer_templates_rate_check
    check (
      minimum_hourly_rate >= 0
      and maximum_hourly_rate >= minimum_hourly_rate
    ),

  constraint demo_freelancer_templates_experience_check
    check (
      minimum_experience_years >= 0
      and maximum_experience_years >= minimum_experience_years
    )
);


create unique index if not exists
demo_freelancer_templates_unique_idx
on public.demo_freelancer_templates (
  lower(category),
  lower(headline)
);


